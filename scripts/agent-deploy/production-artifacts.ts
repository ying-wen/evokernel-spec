export function generateDockerfile(plan: DeploymentPlan): string {
  const e = ENGINE_DOCKER_BASE[plan.recommended.engine];
  if (!e) {
    return `# No Dockerfile template for engine ${plan.recommended.engine} — add to ENGINE_DOCKER_BASE in production-artifacts.ts`;
  }

  return `# Dockerfile generated by agent-deploy (production-grade)
# Model: ${plan.parsed_model.hf_id}
# Engine: ${plan.recommended.engine} ${e.tag_pinned}
# Hardware: ${plan.hardware.name} × ${plan.recommended.card_count}
#
# This is a reproducible build — version-pinned base + deps. For air-gapped
# deployment, mirror the base image to your registry.

FROM ${e.image}:${e.tag_pinned}

# Reproducibility: pin model checkpoint by commit SHA at deploy time
# (replace HEAD with the actual commit hash from the model repo's git log)
ARG MODEL_REVISION=HEAD

# Create non-root runtime user
RUN useradd -m -u 1000 -s /bin/bash llm-runtime
USER llm-runtime
WORKDIR /workspace

${e.setup}
# Pre-fetch model weights — done at build time for cold-start reduction.
# For models > 100GB, consider mounting from PVC instead.
ARG MODEL_ID=${plan.parsed_model.hf_id}
RUN python3 -c "from huggingface_hub import snapshot_download; \\
                snapshot_download(repo_id='\${MODEL_ID}', revision='\${MODEL_REVISION}', \\
                                  local_dir='/workspace/model', local_dir_use_symlinks=False)" \\
    || echo "Model download failed — must mount /workspace/model as volume"

# Health check
HEALTHCHECK --interval=30s --timeout=10s --start-period=120s --retries=3 \\
  CMD curl -f http://localhost:8000/health || exit 1

EXPOSE 8000

ENV PYTHONUNBUFFERED=1
ENV LOG_LEVEL=info

# Engine launch — reads from env to allow rollout-time tuning
ENTRYPOINT ["${e.entrypoint}"]
CMD ["--model-path", "/workspace/model", \\
     "--tp-size", "${plan.recommended.parallelism.tp}", \\
     "--quantization", "${plan.recommended.quantization}", \\
     "--port", "8000"]
`;
}

