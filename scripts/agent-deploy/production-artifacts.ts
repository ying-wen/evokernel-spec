export function generatePrometheusRules(plan: DeploymentPlan): string {
  const r = plan.recommended;
  const m = plan.parsed_model;
  const slug = m.hf_id.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '').slice(0, 50);
  const ttft_target_ms = plan.input.target_ttft_ms ?? 500;
  const cost_target = plan.input.target_cost ?? r.estimated_dollars_per_m_tokens * 1.2;

  return `# Prometheus alert rules — generated by agent-deploy
# Apply: kubectl apply -f prometheus-rules.yaml
groups:
- name: ${slug}-sla
  interval: 30s
  rules:

  # ===== SLA: latency =====
  - alert: TTFT_p99_too_high
    expr: histogram_quantile(0.99, rate(vllm_e2e_request_latency_seconds_bucket{deployment="${slug}"}[5m])) > ${ttft_target_ms / 1000}
    for: 5m
    labels: { severity: warning, sla: latency }
    annotations:
      summary: "TTFT p99 above SLA target"
      description: "p99 = {{ $value }}s, target = ${ttft_target_ms / 1000}s. Check engine queue depth + cold prefill cache."
      runbook: "/runbook.md#ttft-p99"

  - alert: Decode_throughput_dropped
    expr: rate(vllm_generation_tokens_total{deployment="${slug}"}[5m]) < ${Math.floor(r.expected_decode_tok_s_per_card * r.parallelism.tp * 0.7)}
    for: 10m
    labels: { severity: warning, sla: throughput }
    annotations:
      summary: "Decode throughput below 70% of expected"
      description: "Achieved {{ $value }} tok/s (per pod), expected ${Math.floor(r.expected_decode_tok_s_per_card * r.parallelism.tp)}. Check HBM bandwidth utilization."

  # ===== SLA: error rate =====
  - alert: Request_error_rate_high
    expr: rate(vllm_request_failures_total{deployment="${slug}"}[5m]) / rate(vllm_request_total{deployment="${slug}"}[5m]) > 0.01
    for: 5m
    labels: { severity: critical, sla: errors }
    annotations:
      summary: "Error rate > 1%"
      runbook: "/runbook.md#error-rate"

  # ===== Resource: OOM =====
  - alert: GPU_OOM_imminent
    expr: gpu_memory_used_bytes{deployment="${slug}"} / gpu_memory_total_bytes > 0.95
    for: 2m
    labels: { severity: warning, sla: memory }
    annotations:
      summary: "GPU memory > 95% — OOM imminent"
      description: "Reduce max-num-seqs or increase gpu-memory-utilization headroom."
      runbook: "/runbook.md#gpu-oom"

  - alert: GPU_OOM_actual
    expr: increase(gpu_oom_events_total{deployment="${slug}"}[10m]) > 0
    for: 1m
    labels: { severity: critical, sla: memory }
    annotations:
      summary: "Actual OOM event detected"
      runbook: "/runbook.md#gpu-oom"

  # ===== Cost governance =====
  - alert: Cost_per_M_tokens_above_budget
    expr: (sum(gpu_power_watts{deployment="${slug}"}) / 1000 * 0.10 + ${r.card_count} * 3.50) / (sum(rate(vllm_generation_tokens_total{deployment="${slug}"}[1h])) * 3600 / 1e6) > ${cost_target}
    for: 30m
    labels: { severity: warning, sla: cost }
    annotations:
      summary: "$/M tokens above budget target"
      description: "Current ~{{ $value }} \\$/M tok, target = ${cost_target} \\$/M tok. Workload mix may have drifted."

  # ===== Deployment health =====
  - alert: Deployment_unhealthy
    expr: kube_deployment_status_replicas_available{deployment="${slug}-server"} < kube_deployment_spec_replicas{deployment="${slug}-server"}
    for: 5m
    labels: { severity: critical }
    annotations:
      summary: "Replicas not at desired count"
      runbook: "/runbook.md#deployment-unhealthy"

  # ===== Quality drift (advanced — only if eval-as-service is wired up) =====
  - alert: Quality_eval_drift
    expr: abs(eval_score{deployment="${slug}", set="${plan.input.workload}-suite"} - eval_score_baseline{deployment="${slug}", set="${plan.input.workload}-suite"}) > 0.005
    for: 1h
    labels: { severity: warning, sla: quality }
    annotations:
      summary: "Eval score drift > 0.5% from baseline"
      description: "Trigger rollback if drift persists 2 evaluation cycles."
      runbook: "/runbook.md#quality-drift"
`;
}

export function generateRunbook(plan: DeploymentPlan): string {
  const m = plan.parsed_model;
  const r = plan.recommended;
  return `# On-Call Runbook for ${m.hf_id} on ${plan.hardware.name}

> Generated by agent-deploy. Pair with prometheus-rules.yaml + rollback-plan.md.

## Quick reference

| Metric | Target | Alert threshold |
|---|---|---|
| TTFT p99 | ≤ ${plan.input.target_ttft_ms ?? 500}ms | TTFT_p99_too_high |
| Decode tok/s/card | ≥ ${r.expected_decode_tok_s_per_card.toFixed(0)} | Decode_throughput_dropped |
| Error rate | < 1% | Request_error_rate_high |
| $/M tokens | ≤ ${(plan.input.target_cost ?? r.estimated_dollars_per_m_tokens * 1.2).toFixed(2)} | Cost_per_M_tokens_above_budget |
| GPU memory | < 95% | GPU_OOM_imminent |
| Eval drift | < 0.5% | Quality_eval_drift |

## Common alerts

### \`TTFT_p99_too_high\`

**Likely causes**:
1. Cold prefix cache (just deployed / cache evicted)
2. Burst of long-prompt requests
3. Pod memory pressure causing swapping

**First-response (5 min)**:
\`\`\`bash
# Check engine queue depth
kubectl exec -it deploy/${m.hf_id.replace(/[^a-z0-9]+/gi, '-').toLowerCase()}-server -- curl localhost:8000/metrics | grep queue_size

# Check GPU memory
kubectl exec -it deploy/${m.hf_id.replace(/[^a-z0-9]+/gi, '-').toLowerCase()}-server -- nvidia-smi
\`\`\`

**Mitigation**:
- If queue > 50: temporarily increase \`--max-num-seqs\` (engine restart req)
- If GPU mem > 90%: KV-quant FP8 → free up cache
- If long-prompt burst: enable \`--enable-chunked-prefill\` if not already

**Escalate** if persistent > 30 min: trigger rollback (see rollback-plan.md).

### \`GPU_OOM_imminent\` or \`GPU_OOM_actual\`

**Likely causes**:
1. KV cache full (concurrent users × ctx_len > budget)
2. Memory leak in custom kernels
3. Model weight + activation > expected (prompt of unexpected shape)

**First-response (2 min)**:
\`\`\`bash
# Reduce max concurrent requests immediately
kubectl set env deploy/${m.hf_id.replace(/[^a-z0-9]+/gi, '-').toLowerCase()}-server MAX_NUM_SEQS=128
\`\`\`

**Mitigation**:
- Enable KV-cache CPU offload (pattern: hot-cold-kv-tiering)
- Reduce max-model-len if some users don't need full ${m.max_context} tokens
- If OOM persists: rollback + investigate

### \`Quality_eval_drift\`

**Likely causes**:
1. Quantization regression (workload mix shifted to long-context where ${r.quantization} is weaker)
2. Engine bug (recent vLLM/SGLang upgrade)
3. Model corruption / wrong checkpoint loaded

**First-response (15 min)**:
\`\`\`bash
# Verify checkpoint sha matches expected
kubectl exec -it ... -- sha256sum /workspace/model/model.safetensors
# Compare to provenance.json
\`\`\`

**Mitigation**:
- If sha mismatch → critical, rollback immediately
- If sha matches → consider quant rollback (FP4 → FP8 → FP16) per migration playbook
- Run full eval suite + flag for manual review

### \`Cost_per_M_tokens_above_budget\`

Workload mix has likely shifted. Not necessarily an immediate emergency.

**First-response (30 min)**:
- Check workload distribution: short-chat vs long-context vs reasoning
- If long-context grew: cost is structural (longer ctx = more KV reads = more bandwidth)
- If short-chat grew with low concurrency: check batch-size effective utilization

**Mitigation**:
- Tune chunked-prefill chunk size for current workload
- Consider PD-disagg if scale > 16 cards
- Engineer-of-record review monthly

## Diagnostic commands

\`\`\`bash
# Engine state
kubectl exec -it deploy/<name> -- curl localhost:8000/metrics | grep -E "queue|cache|throughput"

# GPU state
kubectl exec -it deploy/<name> -- ${plan.hardware.vendor.id === 'nvidia' ? 'nvidia-smi' : plan.hardware.vendor.id === 'amd' ? 'rocm-smi' : 'npu-smi info'}

# Recent logs (last 5 min)
kubectl logs deploy/<name> --since=5m | grep -iE "error|warn|oom"

# Pod resource use
kubectl top pod -l app=<name>
\`\`\`

## Profiling (when issue is "slow but not failing")

Use ${plan.hardware.vendor.id === 'nvidia' ? '\`ncu\` (deep) or \`nsys\` (timeline)' : plan.hardware.vendor.id === 'amd' ? '\`rocprof\`' : plan.hardware.vendor.id === 'huawei' ? '\`msprof\` (Cube/Vector pipeline view)' : 'vendor profiler'}.

See /dev-toolkit/profiling-tools/ for invocation examples.

## Escalation

| Severity | Action |
|---|---|
| Critical (data loss / outage) | Page primary on-call. Trigger rollback immediately. |
| High (SLA breach > 30 min) | Page primary on-call. |
| Warn (drift / cost) | File ticket; review next morning. |
`;
}

export function generateRollbackPlan(plan: DeploymentPlan): string {
  const m = plan.parsed_model;
  const slug = m.hf_id.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '').slice(0, 50);
  return `# Rollback Plan for ${m.hf_id}

> Generated by agent-deploy. Triggered by runbook on critical SLA breach.

## Rollback budget: 5 minutes (DNS/LB) or 2 minutes (canary weight)

## Pre-flight requirements (must be true BEFORE deploy)

- [x] Old stack remains on standby (DO NOT tear down for ≥ 14 days post-deploy)
- [x] Old stack receives mirror traffic for monitoring
- [x] DNS / LB has weighted routing configured
- [x] Engineer-of-record knows the rollback command
- [x] Last-known-good engine version + model checkpoint pinned

## Rollback procedures (by deployment style)

### A. Kubernetes weighted Service routing

\`\`\`bash
# Shift 100% to old stack
kubectl patch svc ${slug}-svc -p '{"spec":{"selector":{"version":"old"}}}'

# Verify
kubectl get pods -l version=old
curl ${slug}-svc:80/health
\`\`\`

### B. Istio VirtualService (canary weights)

\`\`\`yaml
apiVersion: networking.istio.io/v1
kind: VirtualService
metadata: { name: ${slug} }
spec:
  http:
  - route:
    - destination: { host: ${slug}-svc, subset: old }
      weight: 100      # was 0% before; now 100%
    - destination: { host: ${slug}-svc, subset: new }
      weight: 0        # was 100% before
\`\`\`

### C. DNS-based (slowest, last resort)

Update DNS A/CNAME record to old stack endpoint. **TTL must be ≤ 60s** (configured at deploy time).

## Verification after rollback

- [ ] TTFT p99 returns to baseline within 5 min
- [ ] Error rate drops below 1% within 5 min
- [ ] No user-facing complaints in next 30 min

## Post-incident actions

1. Open incident ticket with timeline + rollback decision rationale
2. Snapshot the new-stack state (logs / metrics / dashboards) — DO NOT delete pods immediately
3. Schedule post-mortem within 5 business days
4. Update runbook with new failure mode if novel
5. Decision: re-roll-out (after fix) vs. retire stack (if fundamental issue)

## Standby stack lifecycle

- **Day 0-14**: Old stack runs at 0% traffic but full capacity (cost ~ same as new stack)
- **Day 14-30**: Old stack scaled to minimum (1 pod) for emergency fallback
- **Day 30+**: Old stack torn down. Re-deploy from CI if needed.
`;
}

