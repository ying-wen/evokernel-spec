/**
 * Production-grade artifact generators (v2.9).
 *
 * The base agent-deploy/index.ts emits the planning artifacts; this module
 * adds artifacts required for production-grade delivery:
 *
 *   1. Dockerfile (reproducible build with pinned versions)
 *   2. kubernetes/deployment.yaml (resource limits + probes)
 *   3. kubernetes/service.yaml + kubernetes/hpa.yaml (autoscaling)
 *   4. prometheus-rules.yaml (SLA + cost + quality alerts)
 *   5. grafana-dashboard.json (observability)
 *   6. runbook.md (on-call response)
 *   7. rollback-plan.md (failure recovery procedure)
 *   8. provenance.json (versioned everything for replay)
 *   9. license-audit.md (compliance check)
 *  10. cost-governance.md (budget caps + drift alerts)
 *  11. production-checklist.md (gating checklist before deploy)
 *  12. sbom.json (software bill of materials)
 *
 * Maps to the production-grade requirements outlined in
 * docs/superpowers/specs/2026-05-02-agent-e2e-sample.md.
 */

interface DeploymentPlan {
  input: { model: string; hardware: string; workload: string; target_cost?: number; target_ttft_ms?: number };
  parsed_model: { hf_id: string; archetype: string; total_params_b: number; active_params_b: number; num_layers: number; max_context: number; num_experts: number };
  hardware: { id: string; name: string; vendor: { id: string }; memory: { capacity_gb: { value: number } } };
  recommended: { engine: string; quantization: string; parallelism: { tp: number; pp: number; ep: number }; card_count: number; expected_decode_tok_s_per_card: number; estimated_dollars_per_m_tokens: number };
  kernel_gaps: Array<{ op: string; missing_on: string; suggestion: string }>;
}

